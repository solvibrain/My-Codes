#include <iostream>
using namespace std;

int main()
{
    int a = 15;
    int b = 017;// this is the octal representation
    int c = 0x0F;// this is the hexadecimal representation
    int d = 0b00001111;

    cout << " The value of the a " << a << endl;
    cout << " The value of the b " << b << endl;
    cout << " The value of the c " << c << endl;
    cout << " The value of the d " << d << endl;
    return 0;
}